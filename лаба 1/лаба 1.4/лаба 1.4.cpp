#include <iostream>
using namespace std;
int main()
{
	float a, b;
	cout << "a=";
	cin >> a;
	cout << "b=";
	cin >> b;
	cout << (a * a) + (b * b) << endl;
	cout << (a * a) - (b * b) << endl;
	cout << (a * a) * (b * b) << endl;
	cout << (a * a) / (b * b) << endl;
}
